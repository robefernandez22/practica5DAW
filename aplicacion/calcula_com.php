<?php

	class Calcula { 

		/** 
		 * suma dos numeros
		 * @param float $a 
		 * @param float $b 
		 * @return float
		*/
		public function suma($a, $b){ 
			return $a+$b;
		}

		/** 
		 * resta dos numeros
		 * @param float $a 
		 * @param float $b 
		 * @return float 
		*/ 
		public function resta($a, $b){
			return $a-$b; 
		}

	}

?>